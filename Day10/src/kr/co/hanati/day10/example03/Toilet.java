package kr.co.hanati.day10.example03;

public class Toilet {
	public void openDoor(String name){
		System.out.println(name + "이 문을 엽니다.");
	}
	
	public void enterToilet(String name){
		System.out.println(name+"이 입장 합니다.");
	}
	
	public void useToilet(String name){
		System.out.println(name+"이 볼일을 봅니다.");
	}
	
	public void closeDoor(String name){
		System.out.println(name+"이 문을 닫습니다.");
	}
	
	public void 명언보기(String name){
		System.out.println(name+"이 명언을 읽습니다.");
	}
}
